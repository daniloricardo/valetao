package nelsys.modelo;


public class BonusProduto {

	private int id;
	private String idfuncao;
	private String idgrupo;
	private String idproduto;
	private Double vlbonus;
	private Double albonus;
	private Double vlbonificacao;
	private Double albonificao;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getIdfuncao() {
		return idfuncao;
	}
	public void setIdfuncao(String idfuncao) {
		this.idfuncao = idfuncao;
	}
	public String getIdgrupo() {
		return idgrupo;
	}
	public void setIdgrupo(String idgrupo) {
		this.idgrupo = idgrupo;
	}
	public String getIdproduto() {
		return idproduto;
	}
	public void setIdproduto(String idproduto) {
		this.idproduto = idproduto;
	}
	public Double getVlbonus() {
		return vlbonus;
	}
	public void setVlbonus(Double vlbonus) {
		this.vlbonus = vlbonus;
	}
	public Double getAlbonus() {
		return albonus;
	}
	public void setAlbonus(Double albonus) {
		this.albonus = albonus;
	}
	public Double getVlbonificacao() {
		return vlbonificacao;
	}
	public void setVlbonificacao(Double vlbonificacao) {
		this.vlbonificacao = vlbonificacao;
	}
	public Double getAlbonificao() {
		return albonificao;
	}
	public void setAlbonificao(Double albonificao) {
		this.albonificao = albonificao;
	}
	
	
}
